
#Exercício Python 006: Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada.

num1 = int(input('Digite um número qualquer:'))

print('O dobro do número {}, é {}'.format(num1 , num1*2))
print('O triplo do número {}, é {}'.format(num1 , num1*3))
print('A raiz quadratida do número {}, é {}'.format(num1 , num1**(1/2)))
